CREATE TABLE RICHIEDE (
    CE_PRENOTAZIONE   VARCHAR2(32)        NOT NULL REFERENCES PRENOTAZIONE(CODICE_PRENOTAZIONE) ON DELETE CASCADE,
    CE_PERSONA_CF     CHAR(16)            NOT NULL,
    CE_CODICE_DOC     VARCHAR2(32)        NOT NULL,
    CONSTRAINT RICHIEDE PRIMARY KEY (CE_PRENOTAZIONE, CE_PERSONA_CF, CE_CODICE_DOC),
    CONSTRAINT RIC_DOC FOREIGN KEY (CE_PERSONA_CF, CE_CODICE_DOC) REFERENCES DOCUMENTO(CE_PERSONA_CF, CODICE_DOCUMENTO) ON DELETE CASCADE
);

CREATE TABLE COMPILA (
