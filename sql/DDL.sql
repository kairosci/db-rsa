CREATE TABLE REGIONE_ITALIANA (
    ID_REGIONE  CHAR(2)     PRIMARY KEY,
    NOME        VARCHAR2(64) NOT NULL UNIQUE
);

CREATE TABLE PROVINCIA_ITALIANA (
    SIGLA       CHAR(2)     PRIMARY KEY,
    NOME        VARCHAR2(64) NOT NULL UNIQUE,
    CE_REGIONE  CHAR(2)     NOT NULL REFERENCES REGIONE_ITALIANA(ID_REGIONE)
);

CREATE TABLE CITTA_ITALIANA (
    ID_COMUNE   CHAR(6)     PRIMARY KEY,
    NOME        VARCHAR2(64) NOT NULL,
    CE_PROVINCIA CHAR(2)    NOT NULL REFERENCES PROVINCIA_ITALIANA(SIGLA)
);

CREATE TABLE TIPO_CONTRATTO_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE TURNO_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE SPECIALIZZAZIONE (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(64) NOT NULL UNIQUE
);

CREATE TABLE QUALIFICA_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE STATO_SALUTE_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE FASCIA_ORARIA_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE ESITO_SOMM_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE STATO_MANUTENZIONE_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE GIORNO_SETTIMANA (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(16) NOT NULL UNIQUE
);

CREATE TABLE TIPOLOGIA_DIETA (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE TIPO_CUOCO_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE SETTORE_MANUTENZIONE_DEF (
    ID          VARCHAR2(16) PRIMARY KEY,
    DESCRIZIONE VARCHAR2(32) NOT NULL UNIQUE
);

CREATE TABLE PERSONA (
    CF                CHAR(16)            PRIMARY KEY,
    NOME              VARCHAR2(64)        NOT NULL,
    COGNOME           VARCHAR2(64)        NOT NULL,
    DATA_NASCITA      DATE                NOT NULL,
    CE_CITTA_NASCITA  CHAR(6)             NOT NULL REFERENCES CITTA_ITALIANA(ID_COMUNE),
    CE_CITTA_RESIDENZA CHAR(6)            NOT NULL REFERENCES CITTA_ITALIANA(ID_COMUNE),
    CONSTRAINT PERSONA_CF_FORMATO CHECK (REGEXP_LIKE(CF, '^[A-Z]{6}[0-9]{2}[A-Z][0-9]{2}[A-Z][0-9]{3}[A-Z]$'))
);

CREATE TABLE ENTE (
    P_IVA             CHAR(11)            PRIMARY KEY,
    NOME_ENTE         VARCHAR2(128)       NOT NULL,
    CE_CITTA_SEDE     CHAR(6)             NOT NULL REFERENCES CITTA_ITALIANA(ID_COMUNE),
    CONSTRAINT ENTE_PIVA_FORMATO CHECK (REGEXP_LIKE(P_IVA, '^[0-9]{11}$'))
);

CREATE TABLE DOCUMENTO (
    CE_PERSONA_CF     CHAR(16)            NOT NULL,
    CODICE_DOCUMENTO  VARCHAR2(32)        NOT NULL,
    CE_ENTE_PIVA      CHAR(11),
    SCADENZA          DATE,
    CONSTRAINT DOCUMENTO PRIMARY KEY (CE_PERSONA_CF, CODICE_DOCUMENTO),
    CONSTRAINT DOC_PERSONA FOREIGN KEY (CE_PERSONA_CF) REFERENCES PERSONA(CF) ON DELETE CASCADE,
    CONSTRAINT DOC_ENTE FOREIGN KEY (CE_ENTE_PIVA) REFERENCES ENTE(P_IVA)
);

CREATE TABLE CONTRATTO (
    ID_CONTRATTO      VARCHAR2(32)        PRIMARY KEY,
    TASSA_DETRATTA    NUMBER(8,2)         NOT NULL,
    PAGA_ORARIA       NUMBER(8,2)         NOT NULL,
    CE_TIPO_CONTRATTO VARCHAR2(16)        NOT NULL REFERENCES TIPO_CONTRATTO_DEF(ID)
);

CREATE TABLE PERSONALE (
    CE_PERSONA_CF     CHAR(16)            PRIMARY KEY,
    DATA_ASSUNZIONE   DATE                NOT NULL,
    CE_TURNO          VARCHAR2(16)        NOT NULL REFERENCES TURNO_DEF(ID),
    CE_CONTRATTO      VARCHAR2(32)        NOT NULL REFERENCES CONTRATTO(ID_CONTRATTO),
    CONSTRAINT PERS_PERSONA FOREIGN KEY (CE_PERSONA_CF) REFERENCES PERSONA(CF) ON DELETE CASCADE
);

CREATE TABLE MEDICO (
    CE_PERSONALE_CF   CHAR(16)            PRIMARY KEY,
    CE_SPECIALIZZAZIONE VARCHAR2(16)      NOT NULL REFERENCES SPECIALIZZAZIONE(ID),
    NUMERO_ALBO       VARCHAR2(16)        NOT NULL UNIQUE,
    CONSTRAINT MED_PERS FOREIGN KEY (CE_PERSONALE_CF) REFERENCES PERSONALE(CE_PERSONA_CF) ON DELETE CASCADE
);

CREATE TABLE OPERATORE (
    CE_PERSONALE_CF   CHAR(16)            PRIMARY KEY,
    CE_QUALIFICA      VARCHAR2(16)        NOT NULL REFERENCES QUALIFICA_DEF(ID),
    CONSTRAINT OP_PERS FOREIGN KEY (CE_PERSONALE_CF) REFERENCES PERSONALE(CE_PERSONA_CF) ON DELETE CASCADE
);

CREATE TABLE REPARTO (
    ID_REPARTO        VARCHAR2(16)        PRIMARY KEY,
    DENOMINAZIONE     VARCHAR2(64)        NOT NULL UNIQUE,
    CE_MEDICO_RESP    CHAR(16),
    CONSTRAINT REP_MEDICO FOREIGN KEY (CE_MEDICO_RESP) REFERENCES MEDICO(CE_PERSONALE_CF)
);

CREATE TABLE CAMERA (
    ID_CAMERA         VARCHAR2(16)        PRIMARY KEY,
    NUMERO_STANZA     NUMBER(8,0)         NOT NULL,
    PIANO             NUMBER(8,0)         NOT NULL,
    LETTI_TOTALI      NUMBER(8,0)         NOT NULL,
    CE_ID_REPARTO     VARCHAR2(16)        NOT NULL,
    CONSTRAINT CAMERA_POSTI CHECK (LETTI_TOTALI BETWEEN 1 AND 4),
    CONSTRAINT CAM_REPARTO FOREIGN KEY (CE_ID_REPARTO) REFERENCES REPARTO(ID_REPARTO) ON DELETE CASCADE
);

CREATE TABLE RESIDENTE (
    CE_PERSONA_CF     CHAR(16)            PRIMARY KEY,
    CE_CAMERA         VARCHAR2(16)        NOT NULL REFERENCES CAMERA(ID_CAMERA),
    CE_MEDICO_CURANTE CHAR(16)            REFERENCES MEDICO(CE_PERSONALE_CF),
    DATA_INGRESSO     DATE                DEFAULT TRUNC(SYSDATE) NOT NULL,
    CE_STATO_SALUTE   VARCHAR2(16)        NOT NULL REFERENCES STATO_SALUTE_DEF(ID),
    CONSTRAINT RES_PERSONA FOREIGN KEY (CE_PERSONA_CF) REFERENCES PERSONA(CF) ON DELETE CASCADE
);

CREATE TABLE FARMACO (
    CODICE_AIC        VARCHAR2(16)        PRIMARY KEY,
    NOME_FARMACO      VARCHAR2(128)       NOT NULL,
    DOSE              VARCHAR2(32)        NOT NULL,
    CE_FASCIA_ORARIA  VARCHAR2(16)        NOT NULL REFERENCES FASCIA_ORARIA_DEF(ID)
);

CREATE TABLE SOMMINISTRAZIONE (
    ID_LOG_SANITARIO  NUMBER              GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    CE_RESIDENTE_CF   CHAR(16)            NOT NULL REFERENCES RESIDENTE(CE_PERSONA_CF) ON DELETE CASCADE,
    CE_FARMACO_AIC    VARCHAR2(16)        NOT NULL REFERENCES FARMACO(CODICE_AIC),
    CE_OPERATORE_CF   CHAR(16)            NOT NULL REFERENCES OPERATORE(CE_PERSONALE_CF),
    DATA_ORA_SOMM     TIMESTAMP(0)        DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CE_ESITO          VARCHAR2(16)        NOT NULL REFERENCES ESITO_SOMM_DEF(ID)
);

CREATE TABLE MALATTIA (
    CODICE_MALATTIA   VARCHAR2(16)        PRIMARY KEY,
    NOME_MALATTIA     VARCHAR2(128)       NOT NULL,
    DESCRIZIONE_MALATTIA VARCHAR2(256),
    LIVELLO_GRAVITA   NUMBER(8,0)         CHECK (LIVELLO_GRAVITA BETWEEN 1 AND 5)
);

CREATE TABLE PAI (
    CODICE_PAI        VARCHAR2(16)        PRIMARY KEY,
    CE_RESIDENTE_CF   CHAR(16)            NOT NULL REFERENCES RESIDENTE(CE_PERSONA_CF) ON DELETE CASCADE,
    DATA_REDAZIONE    DATE                NOT NULL,
    DATA_REVISIONE    DATE,
    DIAGNOSI          VARCHAR2(512),
    OBIETTIVI         VARCHAR2(512),
    TRATTAMENTO       VARCHAR2(512),
    RAGGIUNTO         CHAR(1)             DEFAULT 'N' CHECK (RAGGIUNTO IN ('S', 'N')),
    DATA_DIMISSIONE_PAI   DATE
);

CREATE TABLE DIETA (
    NOME_DIETA        VARCHAR2(64)        PRIMARY KEY,
    CE_TIPOLOGIA      VARCHAR2(16)        NOT NULL REFERENCES TIPOLOGIA_DIETA(ID),
    PATOLOGIA_EXTRA   VARCHAR2(256)
);

CREATE TABLE AZIENDA_ESTERNA (
    P_IVA             CHAR(11)            PRIMARY KEY,
    NOME_AZIENDA      VARCHAR2(128)       NOT NULL,
    SEDE_LEGALE       VARCHAR2(128),
    SEDE_OPERATIVA    VARCHAR2(128),
    CONSTRAINT AZIENDA_PIVA CHECK (REGEXP_LIKE(P_IVA, '^[0-9]{11}$'))
);

CREATE TABLE DITTA_PULIZIA (
