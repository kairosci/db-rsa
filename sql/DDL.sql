);

CREATE TABLE GESTISCE (
    CE_REPARTO        VARCHAR2(16)        NOT NULL REFERENCES REPARTO(ID_REPARTO) ON DELETE CASCADE,
    CE_MEDICO_CF      CHAR(16)            NOT NULL REFERENCES MEDICO(CE_PERSONALE_CF) ON DELETE CASCADE,
    DATA_INIZIO_GESTIONE DATE             DEFAULT TRUNC(SYSDATE) NOT NULL,
    CONSTRAINT GESTISCE PRIMARY KEY (CE_REPARTO, CE_MEDICO_CF)
