    CE_TIPO_CUOCO     VARCHAR2(16)        REFERENCES TIPO_CUOCO_DEF(ID)
);

CREATE TABLE DITTA_MANUTENZIONE (
    CE_PIVA           CHAR(11)            PRIMARY KEY REFERENCES AZIENDA_ESTERNA(P_IVA) ON DELETE CASCADE,
    CE_SETTORE        VARCHAR2(16)        REFERENCES SETTORE_MANUTENZIONE_DEF(ID)
);

CREATE TABLE MANUTENZIONE (
    ID_INTERVENTO     VARCHAR2(16)        PRIMARY KEY,
    CE_DITTA_PIVA     CHAR(11)            NOT NULL REFERENCES DITTA_MANUTENZIONE(CE_PIVA) ON DELETE CASCADE,
    CE_STATO          VARCHAR2(16)        NOT NULL REFERENCES STATO_MANUTENZIONE_DEF(ID),
    DATA_INIZIO_INTERVENTO DATE            NOT NULL,
    DESCRIZIONE_GUASTO VARCHAR2(512),
    COSTO             NUMBER(8,2)
);

CREATE TABLE PRENOTAZIONE (
    CODICE_PRENOTAZIONE VARCHAR2(32)       PRIMARY KEY,
    CE_CAMERA         VARCHAR2(16)        REFERENCES CAMERA(ID_CAMERA),
    CE_RESIDENTE_CF   CHAR(16)            REFERENCES RESIDENTE(CE_PERSONA_CF),
    DATA_PRENOTAZIONE DATE                NOT NULL,
    CF_PRENOTATORE    CHAR(16)            NOT NULL REFERENCES PERSONA(CF),
    DESCRIZIONE_PRENOTAZIONE VARCHAR2(256),
    DATA_DIMISSIONE_PREVISTA DATE,
    GIORNO_PRENOTAZIONE   DATE,
    ORA_INIZIO_PRENOTAZIONE TIMESTAMP(0),
    ORA_FINE_PRENOTAZIONE   TIMESTAMP(0)
);

CREATE TABLE TURNO (
    ID_TURNO          VARCHAR2(16)        PRIMARY KEY,
    CE_PERSONALE_CF   CHAR(16)            NOT NULL REFERENCES PERSONALE(CE_PERSONA_CF) ON DELETE CASCADE,
    GIORNO_TURNO      DATE                NOT NULL,
    ORA_INIZIO_TURNO  TIMESTAMP(0)        NOT NULL,
    ORA_FINE_TURNO    TIMESTAMP(0)        NOT NULL
);

CREATE TABLE PARENTE (
